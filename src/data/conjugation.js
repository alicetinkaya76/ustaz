/**
 * Bir kök+bâb için çekim tablosu getir
 * @param {string} root — ör. "ع-ل-م"
 * @param {string} bab — ör. "I", "II", "X"
 * @returns {object|null} — { root, bab, meaning, base_madi, base_mudari, madi[], mudari[], amr[] }
 */
export function getConjugation(root, bab) {
  const key = `${root}_${bab}`;
  return conjugationDB[key] || null;
}

/**
 * Bir kök için mevcut tüm bâb çekimlerini getir
 * @param {string} root
 * @returns {object[]}
 */
export function getConjugationsForRoot(root) {
  return Object.values(conjugationDB).filter(c => c.root === root);
}

/**
 * Veritabanındaki toplam çekim sayısı
 */
export function getConjugationCount() {
  return Object.keys(conjugationDB).length;
}

/**
 * Belirli bir fiil formu için çekim bul
 * @param {string} verbForm — ör. "نَعْبُدُ"
 * @returns {object|null} — { conjugation, tense, pronounIndex }
 */
export function findConjugationByForm(verbForm) {
  const clean = verbForm.replace(/[\u0670\u0671]/g, "ا"); // normalize alef
  for (const conj of Object.values(conjugationDB)) {
    for (let i = 0; i < 13; i++) {
      if (conj.madi[i] === clean) return { conjugation: conj, tense: "madi", pronounIndex: i };
      if (conj.mudari[i] === clean) return { conjugation: conj, tense: "mudari", pronounIndex: i };
    }
    for (let i = 0; i < 5; i++) {
      if (conj.amr[i] === clean) return { conjugation: conj, tense: "amr", pronounIndex: i };
    }
  }
  return null;
}

export default conjugationDB;
